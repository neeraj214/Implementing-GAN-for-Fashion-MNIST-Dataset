from tensorflow import Tensor
from tensorflow.keras.layers import Input
from setup.train_config import IMAGE_SIZE, CHANNELS

def build_discriminator_input(image_size: int = IMAGE_SIZE, channels: int = CHANNELS) -> Tensor:
    """
    Creates the input layer for the discriminator model.

    This layer accepts either real images from the dataset or fake images
    generated by the generator. Both types of images have the shape (28, 28, 1).

    Args:
        image_size (int): The width and height of the image (default: IMAGE_SIZE).
        channels (int): The number of image channels (default: CHANNELS).

    Returns:
        Tensor: The input tensor for the discriminator model of shape (image_size, image_size, channels).
    """
    inputs = Input(shape=(image_size, image_size, channels), name="image_input")
    return inputs
